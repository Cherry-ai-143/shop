async function seedDatabase() {
  const client = new MongoClient(uri)

  try {
    await client.connect()
    console.log("[v0] Connected to MongoDB")

    const db = client.db("smart-compare")
    const collection = db.collection("products")

    // Clear existing data
    await collection.deleteMany({})
    console.log("[v0] Cleared existing products")

    // Insert sample products
    const result = await collection.insertMany(sampleProducts)
    console.log(`[v0] Inserted ${result.insertedCount} products`)

    // Create index on category for faster queries
    await collection.createIndex({ category: 1 })
    console.log("[v0] Created index on category field")

    console.log("[v0] Database seeding completed successfully!")
  } catch (error) {
    console.error("[v0] Error seeding database:", error)
  } finally {
    await client.close()
  }
}

